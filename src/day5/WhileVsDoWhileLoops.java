package day5;

public class WhileVsDoWhileLoops {

	public static void main(String[] args) {
	// EXAMPLE FOR WHILE 
		int i=96;
		while(i<=8) {
			System.out.println(i);
			i++;
		}
	// EXAMPLE FOR DO WHILE
		int o=98;
		do {
			System.out.println(o);
			o++;
		}while(o>=698);

		//for both statements condition is false but the do while loop will print statement at least once
	}

}
