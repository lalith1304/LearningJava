package day2;

public class Data_Types {

	public static void main(String[] args) {
		
      // NUMERIC DATA TYPES.
		
		byte by=68;
		System.out.println(by);
		
		short sh=689;
		System.out.println(sh);
		
		int n=668899;
		System.out.println(n);
		
		long l=666688889999l; // literal is needed
		System.out.println(l);
		
		// when we need to add concatenation and sum than
		
		int a=66;
		int b=88;
		int c=99;
		System.out.println("sum of a , b , c is :"+(a+b+c));
		
	  // DECIMAL DATA TYPES.
		
		float f=689.689f; // literal is needed
		System.out.println(f);
		
		double d=668899.666888999;
		System.out.println(d);
		
	  // CHAR DATA TYPE.	
		char ch='V';
		System.out.println(ch);
		
	  // STRING DATA TYPE.
		String s="lalith";
		System.out.println(s);
		
	  // BOOLEAN DATA TYPE.
		boolean b1=true;
		System.out.println(b1);
		 
		
		
		
		
		
		
		
		
	}

}
