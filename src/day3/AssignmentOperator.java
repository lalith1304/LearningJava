package day3;

public class AssignmentOperator {

	public static void main(String[] args) {
		
	// ASSIGNMENT OPERATORS(=    +=    -=    *=    /=    %=)
		
		   int a=9;
		   
		   a+=9;
		   System.out.println(a);// 18
		   
		   a-=9;
		   System.out.println(a);// 9
		   
		   a*=9;
		   System.out.println(a);// 81
		   
		   a/=9;
		   System.out.println(a);// 9
		   
		   a%=9;
		   System.out.println(a);// 0
		   
		   a=9;
		   System.out.println(a);// 9
		   

	}

}
